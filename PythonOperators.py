x = 9
y = 3

#Arithmetic Operators
print(x+y)		#Addition
print(x-y)		#Subtraction
print(x*y)		#Multiplication
print(x/y)		#Division
print(x%y)		#Modulus
print(x**y)		#Exponentiation
print(x//y)		#Floor Division; how many times 'x' can be evenly divided by 'y' without remainder

#Assignment Operators
x = 9			#sets initial value of x to 9
x += 3		#sets value of x to previous value of x plus 3
print(x)

x = 9
x -= 3		#sets value of x to previous value of x minus 3
print(x)
x *= 3		#sets value of x to previous value of x times 3
print(x)
x /= 3		#sets value of x to previous value of x divided by 3
print(x)
x **= 3		#sets value of x to previous value of x to the power of 3
print(x)

#Comparison Operators
x = 9
y = 3
print(x==y)			#True if x = y, False otherwise
print(x!=y)			#True if x ≠ y, False otherwise
print(x>y)			#True if x > y, False otherwise
print(x<y)			#True if x < y, False otherwise
print(x>=y)			#True if x ≥ y, False otherwise
print(x<=y)			#True if x ≤ y, False otherwise
